# to get certain character in the string use index

greetings = "Hello World"

# Specify the index
x = greetings[-5]
print(x)

# slicing is getting a oart of the string

print(greetings[0:5])

print(greetings[6:11])
# simpler version
print(greetings[6:])

name = "java, php, python, c++"

